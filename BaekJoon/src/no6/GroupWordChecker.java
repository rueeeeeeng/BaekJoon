package no6;

import java.util.Scanner;

public class GroupWordChecker {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		
		int number = sc.nextInt();
		String[] words = new String[number];
		
		for (int i = 0; i < words.length; i++) {
			words[i] = sc.next();
		}
		
		
	}

}
